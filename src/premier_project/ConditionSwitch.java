package premier_project;

public class ConditionSwitch {

	public static void main(String[] args) {
		int note = 17;
		switch (note) {
		case 10:
			System.out.println("vous avez eu 10");
			break;
		case 15:
			System.out.println("15");
			break;
		case 14:
			System.out.println("vous avez eu 14");
			break;
		case 13:
			System.out.println("vous avez eu 13");
			break;
		case 12:
			System.out.println("vous avez eu 12");
			break;
		default:
			System.out.println("aucune de valeure");
			break;
		}

	}

}
